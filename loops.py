# Loops
foods = ['apples', 'bread', 'milk']

# for, repeat as many times as elements in the list
for food in foods:
    print(food) # > "apples", "bread", "milk"

# While, be repeating the action while the condition is true
count = 4

while count <= 10:
    print(count)
    count = count + 1 # have to has a code where the counter have to increment